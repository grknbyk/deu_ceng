public class TargetSquare {
	private double totalgain;
	private int aimx;
	private int aimy;
	
	public TargetSquare(double totalgain,int aimx,int aimy) {
		this.totalgain=totalgain;
		this.aimx=aimx;
		this.aimy=aimy;
			
	}


	public double getTotalgain() {
		return totalgain;
	}


	public void setTotalgain(int totalgain) {
		this.totalgain = totalgain;
	}


	public int getAimx() {
		return aimx;
	}


	public void setAimx(int aimx) {
		this.aimx = aimx;
	}


	public int getAimy() {
		return aimy;
	}


	public void setAimy(int aimy) {
		this.aimy = aimy;
	}





}
